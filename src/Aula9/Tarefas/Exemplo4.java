package Aula9.Tarefas;

public class Exemplo4 {
    public static void main(String[] args) {
        
        int i = 1000;
        
        while(i >= 0) {
            System.out.println(i);
            i--;
        }
        
        
        System.out.println("================================");
        for (int j = 1000; j >= 0; j--) {
            System.out.println(j);
        }
    }
}
