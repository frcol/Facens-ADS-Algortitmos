package Aula3;

public class Calculadora {
    public static void main(String[] args) {
        int var1 = 5;
        int var2 = 3;
        
        int varSoma = var1 + var2;
        int varSub = var1 - var2;
        int varMult = var1 * var2;
        int varDiv = var1 / var2;
        int resto = var1 % var2;
        
        System.out.println(varSoma);
        System.out.println(varSub);
        System.out.println(varMult);
        System.out.println(varDiv);
        System.out.println(resto);
    }
}
